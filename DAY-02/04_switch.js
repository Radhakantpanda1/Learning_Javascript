// switch case 
let fruit="mangoes";
switch (fruit) {
    case "mangoes": alert("price is 200rs per kg");
           break;
    case "banana": alert("price is 181rs per kg");
           break;
    case "guava": alert("price is 180rs per kg");
           break;
    case "avocado": alert("price is 1200rs per kg");
           break;

    default: alert(`we are sorry we don't have ${fruit}`);
        break;
}
